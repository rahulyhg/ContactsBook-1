package com.elderj.contactsbook;

public class Org {

    public final int id;
    public final String name;
    public final String email;
    public final String phone;

    public Org(int id, String name, String email, String phone) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.phone = phone;
    }

}
